package com.sdk.oms.shopee.model.common;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonValue;

public enum FeeType {
    UNRECOGNIZED,
    SIZE_SELECTION,
    SIZE_INPUT,
    FIXED_DEFAULT_PRICE,
    CUSTOM_PRICE;

    @JsonCreator
    public static FeeType value(String v) {
        if (v == null) return UNRECOGNIZED;
        try {
            return valueOf(v);
        } catch (IllegalArgumentException e) {
            return UNRECOGNIZED;
        }
    }

    @JsonValue
    public String value() {
        return name();
    }
}
